/* 
Declarando variáveis

- Var: Não é muito recomendado pois é declarada como variável global
- const: recomendado, variável constante
- let: recomendado, altera o valor
*/

var message_var = "Hello var!"; 
const message_const = "Hello const!";
let message_let = "Hello let!";


/* 
Null and Undefineds

- Null != Undefined
*/
const x = null;
const y = undefined;